# 5. membership operators

#     in

#     not in



# in --> it will check the value is in data retuns true

x = 'python'

print('p' in x)
print('1' in x)

x = [10,20,30]

print(10 in x)


# not in ---> it will check the value is not in data retuns true

x = [10,20,30]

print(100 not in x)

print(10 not in x)