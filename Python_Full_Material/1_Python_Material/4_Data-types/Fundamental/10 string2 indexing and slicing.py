#string slicing: Extracting particular portion of a string

x="python program"
print(x)
print(x[0:6])  #upper bond excluded
print(x[0:4])
print(x[7:11])
print(x[-7:-3])
print(x[11:7])  #Empty string, always lower bond to upper bond
print(x[-4:-8])
print(x[7:14])
print(x[7: ])
print(x[ :6])
print(x[ : ])
print(x)
