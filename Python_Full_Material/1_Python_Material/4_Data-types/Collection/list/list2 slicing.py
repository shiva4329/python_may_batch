#List slicing: Extracting elements of a list

x=[10,20,30,40,50,60]

print(x)

print(x[3])

print(x[-2])

#extract------>10 to 40
print(x[0:4])

#Extract ------>30,40,50
print(x[2:5])

print(x[5:2]) #empty list

#Extract ------>30,40,50 using -ve index
print(x[-4:-1])

#Extract 30 to 60
print(x[2:6])

print(x[2: ])

print(x[ :3])

print(x[ : ])
print(x)

print(x[1:-2])






