

#while loop : while loop executes set of stmts for repeated no of times.

#syntax:
'''
initialization
while(condition):
    stmt1
    stmt2
    stmt3
    increment/decrement

all the stmts within while loop should follow space indentation.

The stmts within while loop will be executed untill the condition becomes false

if condition becomes false , then ctrl comes out of while loop.
'''

#program to print no's from 1 to 10 using while loop

x=1
while(x<=10):
    print(x)
    x=x+1
print("end")














