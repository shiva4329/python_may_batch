#Membership Operators : These operators are used to check whether a element is present or not in
#                       a collection or sequence.
'''
Membersship operators are also of 2 types:
    i)in
    ii)not in '''

x=[10,20,30,40,50]

print(10 in x)
print(70 not in x)
print(50 not in x)

y="python"

print('p' in y)
print('a' in y)
print('a' not in y)

