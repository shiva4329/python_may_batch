#Comparision Operators : These operators are used to compare the contents of
#2 objects.

#Comparision operators returns either True or False

#Various comparision operators are : > , < ,>= ,<= ,== ,!=

#Program illustrating Comparision operators

x=10
y=5

print(x>y)

print(x<y)

print(x>=y)

print(x<=y)

print(x==y)

print(x!=y)


