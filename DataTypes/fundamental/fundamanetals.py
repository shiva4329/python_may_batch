# integer -- numbers with out decimal points(int())

x = 10
print(x)
print(type(x)) # type() returns the type of data


# float - number with decimal points(float())

x = 10.12
print(x)
print(type(x)) # type() returns the type of data


# string - combination of letters (str())
# string can be represented in quotes (single/double quotes ) '...'/"...."

x = "hello"
print(x)
print(type(x)) # type() returns the type of data


# boolen - True or False (bool())

x = True
print(x)
print(type(x)) # type() returns the type of data

y = False
print(y)
print(type(y))


# complex ---combination of real and imaginary numbers - a+bj (complex())

x = 10+12j
print(x)
print(type(x))

# real part in complex
print(x.real) # returns the real value in float

# imaginary part in complex
print(x.imag) # returns the imaginary value in float
